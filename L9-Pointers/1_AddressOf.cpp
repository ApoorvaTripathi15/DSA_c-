#include <iostream>
using namespace std;

int main() {


	int a = 10;
	float f = 10.11;
	double d = 100.11;

	cout << "a : " << a << endl;
	cout << "sizeof(&a) : " << sizeof(&a) << " bytes" << endl;
	cout << "&a : " << &a << endl;
	cout << "f : " << f << endl;
	cout << "sizeof(&f) : " << sizeof(&f) << " bytes" << endl;
	cout << "&f : " << &f << endl;
	cout << "d : " << d << endl;
	cout << "sizeof(&d) : " << sizeof(&d) << " bytes" << endl;
	cout << "&d : " << &d << endl;

	return 0;
}
















